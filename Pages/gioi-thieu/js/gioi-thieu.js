document.addEventListener('DOMContentLoaded', function() {
    const mediaLinks = document.querySelectorAll('.introduce_kn_2_0_0__logoMedia');
    const mediaLeftBox = document.querySelector('.introduce_kn_2_0_0__boxMediaLeft');

    // Hàm hiển thị dữ liệu tương ứng khi click vào logo
    function displayMediaData(index) {
        const media = dataMedia[index];
        let htmlContent = '';

        media.post.forEach(post => {
            htmlContent += `
                <div class="introduce_kn_2_0_0__postBox">
                    <h1 class="introduce_kn_2_0_0__postTitle">${post.title}</h1>
                    <p class="introduce_kn_2_0_0__postDate">${post.date} - Nguồn: <span> ${media.brand} <span> </p>
                    <div class="introduce_kn_2_0_0__postContent">${post.content}</div>
                    <a href="${post.path}" target="_blank" class="introduce_kn_2_0_0__postPath">Xem thêm</a>
                </div>
            `;
        });

        mediaLeftBox.innerHTML = htmlContent;
    }

    // Thêm sự kiện click cho mỗi logo
    mediaLinks.forEach((link, index) => {
        link.addEventListener('click', function(event) {
            event.preventDefault(); // Ngăn chặn hành vi mặc định của thẻ a
            displayMediaData(index);
        });
    });

    // Hiển thị bài viết đầu tiên khi trang được tải
    displayMediaData(0);
});






